		<div class="descbox2"> 
			<div id="company2">
				<p>In 1980, Elyse Kroll developed a forum to present emerging, innovative designers. Her exhibition, held at a world-class hotel famous for 
				its elegance and service, set ENK apart from the anonymous booth shows that were prevalent at the time. ENK’s unique production introduced 
				exhibitors, the media and an influential list of buyers to a new fashion experience. ENK Productions, as the company was first named, became 
				an instant success with designers such as Jhane Barnes, Paul Smith and Hugo Boss. In 1986, responding to the demand from the womenswear market 
				for a show of its own, ENK created Fashion Coterie. </p>
				<p>In 1987, Accessorie Circuit was added to begin the triumvirate that quickly became ENK International. The tremendous response by the global 
				marketplace continues to prove that ENK International’s exhibitions are not only eagerly anticipated bi-annual and tri-annual events but also 
				clearly responsible for elevating New York City alongside Paris and Milan as an international fashion capital.</p>
