          <h3>Taxis</h3>
          <p>The base rate in Las Vegas is $2.60 per mile. There is an initial charge of $3.30 when the meter starts to run.  
          If you are starting a trip from the airport, there is an additional charge of $1.80 McCarran Airport fee. 
          There is a 5 person maximum and the fare is the same no matter how many passengers are traveling.   
          Be sure you have enough cash because most Taxis do not accept credit cards. 
          There is a $3 fee for use of a credit/debit card.</p>
          <p>To help calculate your taxi fare, Use <a href="http://www.taxifarefinder.com/main.php?city=LV" target="_blank">Taxifarefinder.com</a></p>

