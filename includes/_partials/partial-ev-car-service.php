          <h3>The Las Vegas Monorail</h3>
          <p>The Las Vegas Monorail runs from the MGM Grand to the Sahara hotel. 
          The route has <strong>seven stations: MGM Grand, Bally's / Paris, Flamingo, Harrah's / Imperial Palace, 
          Las Vegas Convention Center, Hilton and the Sahara.</strong> </p>
          
          <p>The monorail is open Monday - Thursday, 7:00 a.m. to 2:00 a.m. and Friday - Sunday, 7 a.m. - 3 a.m. 
          Rates for the monorail are <strong>$5 for one ride, $12 for an unlimited one-day pass and $28 for an unlimited three-day pass.</strong></p>
